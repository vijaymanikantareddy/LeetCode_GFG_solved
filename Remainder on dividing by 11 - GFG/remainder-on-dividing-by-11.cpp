class Solution
{

public:
    int xmod11(string x)
    {
       // code here
       int sum = 0;
       for(int i = 0 ; i < x.size() ; i++){
           int d = x[i]-'0';
           sum = sum%11;
           sum = sum*10+d;
       }
       return sum%11;
    }
};


