class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        int res=0;
        while(m|n){
            if((m&1)==(n&1)){
                res++;
            }
            else{
                break;
            }
            m>>=1;
            n>>=1;
        }
        if(m==0 and n==0){
            return -1;
        }
        return res+1;
    }
};

