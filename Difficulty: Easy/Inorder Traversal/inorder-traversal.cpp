class Solution {
  public:
    // Function to return a list containing the inorder traversal of the tree.
    void inor(Node* root, vector<int>& res)
    {
        if(root==NULL)
            return ;
        inor(root->left, res);
        res.push_back(root->data);
        inor(root->right, res);
    }
    vector<int> inOrder(Node* root) {
        // Your code here
        vector<int> res;
        inor(root, res);
        return res;
    }
};

