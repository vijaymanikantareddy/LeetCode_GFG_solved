//User function template for C++
class Solution{
public:
	
	int findMaximum(int arr[], int n) {
	    // code here
	    int l=0, r=n-1, mx = arr[0];
	    while(l<=r){
	        mx = arr[l]>mx? arr[l]: mx;
	        mx = arr[r]>mx? arr[r]: mx;
	        l++;
	        r--;
	    }
	    return mx;
	}
};

