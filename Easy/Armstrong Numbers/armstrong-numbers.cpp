class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int res = 0, dup = n;
        while(n > 0){
            int a = n%10;
            n /= 10;
            res += pow(a, 3);
        }
        if(res == dup) return "true";
        return "false";
    }
};

